// há três formas de declarar variaveis: 

// variavel local:

var Nome = 'Bruna';
console.log(Nome);

// variavel global:

Sobrenome = "João";
console.log(Sobrenome);

// variavel sem valor, podendo colocar o valor durante a programcao

var SemValor;

SemValor = "kayo";
console.log(SemValor);

// pode-se usar $ e _ no incio do nome da variavel, nao e aceito numeros e simbolos.

// outra forma de declarar variaveis sao:

let NomeESobrenome = "Bruna Carla";
console.log(NomeESobrenome);

const NomeSobrenome = "Kayo Cicero";
console.log(NomeSobrenome);